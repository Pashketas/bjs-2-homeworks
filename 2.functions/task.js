function getArrayParams(arr) {
  let min, max, sum, avg;
  min = arr[0];
  max = arr[0];
  sum = 0;
  for (let i = 1; i < (arr.length + 1); i++) {
    if (arr[i] > max) {
      max = arr[i];
    } else if (arr[i] < min) {
      min = arr[i];
    }
    sum = sum + arr[i - 1]; 
  }
  avg = Number(((sum / arr.length) .toFixed(2)));
  return { min: min, max: max, avg: avg };
}

